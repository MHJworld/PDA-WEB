package pda;

public class Pda {

	private int pdaID;
	private String pdaTitle;
	private String userID;
	private String pdaDate;
	private String pdaContent;
	private int pdaAvailable;
	private String pdaFileName;
	private String pdaFileRealName;
	
	public int getPdaID() {
		return pdaID;
	}
	public void setPdaID(int pdaID) {
		this.pdaID = pdaID;
	}
	public String getPdaTitle() {
		return pdaTitle;
	}
	public void setPdaTitle(String pdaTitle) {
		this.pdaTitle = pdaTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getPdaDate() {
		return pdaDate;
	}
	public void setPdaDate(String pdaDate) {
		this.pdaDate = pdaDate;
	}
	public String getPdaContent() {
		return pdaContent;
	}
	public void setPdaContent(String pdaContent) {
		this.pdaContent = pdaContent;
	}
	public int getPdaAvailable() {
		return pdaAvailable;
	}
	public void setPdaAvailable(int pdaAvailable) {
		this.pdaAvailable = pdaAvailable;
	}
	public String getPdaFileName() {
		return pdaFileName;
	}
	public void setPdaFileName(String pdaFileName) {
		this.pdaFileName = pdaFileName;
	}
	public String getPdaFileRealName() {
		return pdaFileRealName;
	}
	public void setPdaFileRealName(String pdaFileRealName) {
		this.pdaFileRealName = pdaFileRealName;
	}
}
